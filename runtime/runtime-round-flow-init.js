// Runtime helper: builds round flow controller with current app callbacks/state.

import { createRoundFlowController } from "../flows/round-flow-controller.js";

export function ensureRuntimeRoundFlowController({
  roundFlowController,
  state,
  stateDomainConfig,
  activeStepCount,
  activeBeadMinIndex,
  activeBeadMaxIndex,
  dragCommitMinPx,
  wheelMomentumMs,
  wheelMinSettleMs,
  wheelMaxSettleMs,
  roundLoaderMs,
  computeSettleTarget,
  clamp,
  resetProgress,
  advanceRound,
  getWheelIndex,
  setWheelIndex,
  getBeadPitch,
  getSoundMode,
  applyStepDelta,
  animateOffset,
  animateWheelTo,
  triggerHaptic,
  onStepCommitted,
  stopMantraPlayback,
  stopStepPlayback,
  isRoundLoaderOpen,
  openRoundLoader,
  closeRoundLoader,
  openResetConfirmModal,
  closeResetConfirmModal,
  requestRender,
  displayStep,
  updateCounterText,
  saveState,
  render,
}) {
  if (roundFlowController) return roundFlowController;

  return createRoundFlowController({
    state,
    stateDomainConfig,
    activeStepCount,
    activeBeadMinIndex,
    activeBeadMaxIndex,
    dragCommitMinPx,
    wheelMomentumMs,
    wheelMinSettleMs,
    wheelMaxSettleMs,
    roundLoaderMs,
    computeSettleTarget,
    clamp,
    resetProgress,
    advanceRound,
    getWheelIndex,
    setWheelIndex,
    getBeadPitch,
    getSoundMode,
    applyStepDelta,
    animateOffset,
    animateWheelTo,
    triggerHaptic,
    onStepCommitted,
    stopMantraPlayback,
    stopStepPlayback,
    isRoundLoaderOpen,
    openRoundLoader,
    closeRoundLoader,
    openResetConfirmModal,
    closeResetConfirmModal,
    requestRender,
    displayStep,
    updateCounterText,
    saveState,
    render,
  });
}
